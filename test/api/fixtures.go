// WaitForImageReady polls until the image appears in the region with state ready.
// Uses a 1-hour timeout to accommodate image download and import times.
func WaitForImageReady(c *APIClient, ctx context.Context, config *TestConfig, imageID string) {
	GinkgoWriter.Printf("Waiting for image %s to be ready\n", imageID)

	Eventually(func() bool {
		images, err := c.ListImages(ctx, config.OrgID, config.RegionID)
		Expect(err).NotTo(HaveOccurred())

		for _, image := range images {
			if image.Metadata.Id == imageID {
				GinkgoWriter.Printf("Image %s state: %s\n", imageID, image.Status.State)
				return image.Status.State == regionopenapi.ImageStateReady
			}
		}

		GinkgoWriter.Printf("Image %s not yet visible in list\n", imageID)

		return false
	}).WithTimeout(time.Hour).WithPolling(15 * time.Second).Should(BeTrue())
}

// WaitForLoadBalancerProvisioned polls until the load balancer reaches Provisioned.
func WaitForLoadBalancerProvisioned(c *APIClient, ctx context.Context, lbID string) {
	GinkgoWriter.Printf("Waiting for load balancer %s to be provisioned\n", lbID)

	Eventually(func() coreapi.ResourceProvisioningStatus {
		lb, err := c.GetLoadBalancer(ctx, lbID)
		if err != nil {
			GinkgoWriter.Printf("Error retrieving load balancer %s: %v\n", lbID, err)
			return ""
		}

		return lb.Metadata.ProvisioningStatus
	}).WithTimeout(5*time.Minute).WithPolling(10*time.Second).
		Should(Equal(coreapi.ResourceProvisioningStatusProvisioned),
			"load balancer should eventually be provisioned")
}

// WaitForLoadBalancerGone polls until GET for the load balancer returns the
// expected not-found error.
func WaitForLoadBalancerGone(c *APIClient, ctx context.Context, lbID string) {
	Eventually(func() error {
		_, err := c.GetLoadBalancer(ctx, lbID)
		return err
	}).WithTimeout(2*time.Minute).WithPolling(2*time.Second).
		Should(And(HaveOccurred(), MatchError(coreclient.ErrResourceNotFound)),
			"load balancer should eventually be deleted")
}

// WaitForNetworkVisible polls until the network is readable at the API. The
// region API is eventually consistent on reads: writes are synchronous but
// GETs are served from the controller-runtime cache, so a just-created network
// can briefly 404. A dependent create that resolves the network reference
// (e.g. a load balancer) reads through the same cache and would fail with that
// transient 404, so await visibility before referencing it.
func WaitForNetworkVisible(c *APIClient, ctx context.Context, networkID string) {
	Eventually(func() error {
		_, err := c.GetNetwork(ctx, networkID)
		return err
	}).WithTimeout(5*time.Second).WithPolling(250*time.Millisecond).
		Should(Succeed(), "network should become visible at the API")
}

// WaitForNetworkProvisioned polls until the network reaches the provisioned state.
func WaitForNetworkProvisioned(c *APIClient, ctx context.Context, networkID string) {
	Eventually(func() coreapi.ResourceProvisioningStatus {
		network, err := c.GetNetwork(ctx, networkID)
		if err != nil {
			GinkgoWriter.Printf("Error retrieving network %s: %v\n", networkID, err)
			return ""
		}

		return network.Metadata.ProvisioningStatus
	}).WithTimeout(5*time.Minute).
		WithPolling(5*time.Second).
		Should(Equal(coreapi.ResourceProvisioningStatusProvisioned), "network should be provisioned")
}

// errResourceStillExists signals a resource is still present during a deletion wait.
var errResourceStillExists = errors.New("resource still exists")

// waitForResourceGone polls getByID until the resource is absent
// (coreclient.ErrResourceNotFound), failing if it still exists after the timeout.
func waitForResourceGone(resource, id string, getByID func() error) {
	Eventually(func() error {
		err := getByID()
		if errors.Is(err, coreclient.ErrResourceNotFound) {
			return nil
		}
		if err != nil {
			return err
		}

		return fmt.Errorf("%s %s: %w", resource, id, errResourceStillExists)
	}).WithTimeout(10*time.Minute).
		WithPolling(10*time.Second).
		Should(Succeed(), fmt.Sprintf("%s should be deleted", resource))
}

