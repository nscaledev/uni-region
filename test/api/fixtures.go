const (
	maxResourceNameLength          = 63
	randomResourceSuffixLength     = 8
	testResourceRunIDMaxLength     = 20
	testResourceAttemptMaxLength   = 6
	testResourceLocalUserMaxLength = 12

	legacyTestResourceNamePrefix = "ginkgo-test-"
)

// TestResourceNamePrefix marks every resource name generated by the integration
// suite. The complete generated name embeds repo, run ID/attempt, Unix timestamp,
// resource kind, and a random suffix, e.g.
// auto-uni-region-29234292437-1-1784023200-lb-a1b2c3d4.
const TestResourceNamePrefix = "auto-uni-region-"

var testResourceRunNamePrefix = buildTestResourceRunNamePrefix(time.Now(), os.Getenv)

// UniqueName builds a collision-resistant resource name carrying
// TestResourceNamePrefix so leaked resources can be reliably swept.
func UniqueName(prefix string) string {
	return uniqueName(testResourceRunNamePrefix, prefix, uuid.NewString()[:randomResourceSuffixLength])
}

// IsTestResourceName reports whether name was generated by UniqueName. Legacy
// ginkgo-test-* names stay recognised so the pre-suite sweep can still reclaim
// leaked resources from runs created before the automation prefix changed.
func IsTestResourceName(name string) bool {
	return strings.HasPrefix(name, TestResourceNamePrefix) ||
		strings.HasPrefix(name, legacyTestResourceNamePrefix)
}

func buildTestResourceRunNamePrefix(now time.Time, getenv func(string) string) string {
	timestamp := fmt.Sprintf("%d", now.UTC().Unix())
	basePrefix := strings.TrimSuffix(TestResourceNamePrefix, "-")

	runID := truncateNamePart(sanitizeNamePart(getenv("GITHUB_RUN_ID")), testResourceRunIDMaxLength)
	if runID != "" {
		attempt := truncateNamePart(sanitizeNamePart(getenv("GITHUB_RUN_ATTEMPT")), testResourceAttemptMaxLength)
		if attempt == "" {
			attempt = "1"
		}

		return strings.Join([]string{basePrefix, runID, attempt, timestamp}, "-")
	}

	user := truncateNamePart(sanitizeNamePart(getenv("USER")), testResourceLocalUserMaxLength)
	if user == "" {
		user = "unknown"
	}

	return strings.Join([]string{basePrefix, "local", user, timestamp}, "-")
}

func uniqueName(runPrefix, resourceKind, suffix string) string {
	runPrefix = strings.Trim(sanitizeNamePart(runPrefix), "-")
	resourceKind = sanitizeNamePart(resourceKind)
	suffix = truncateNamePart(sanitizeNamePart(suffix), randomResourceSuffixLength)

	if runPrefix == "" {
		runPrefix = strings.TrimSuffix(TestResourceNamePrefix, "-")
	}

	if resourceKind == "" {
		resourceKind = "resource"
	}

	if suffix == "" {
		suffix = uuid.NewString()[:randomResourceSuffixLength]
	}

	maxResourceKindLength := maxResourceNameLength - len(runPrefix) - len(suffix) - 2
	if maxResourceKindLength < 1 {
		runPrefix = truncateNamePart(runPrefix, maxResourceNameLength-len("-r-")-len(suffix))
		maxResourceKindLength = maxResourceNameLength - len(runPrefix) - len(suffix) - 2
	}

	resourceKind = truncateNamePart(resourceKind, maxResourceKindLength)
	if resourceKind == "" {
		resourceKind = "r"
	}

	return fmt.Sprintf("%s-%s-%s", runPrefix, resourceKind, suffix)
}

func sanitizeNamePart(value string) string {
	value = strings.ToLower(value)

	var builder strings.Builder
	lastHyphen := false

	for _, r := range value {
		if (r >= 'a' && r <= 'z') || (r >= '0' && r <= '9') {
			builder.WriteRune(r)
			lastHyphen = false

			continue
		}

		if builder.Len() == 0 || lastHyphen {
			continue
		}

		builder.WriteByte('-')
		lastHyphen = true
	}

	return strings.Trim(builder.String(), "-")
}

func truncateNamePart(value string, maxLength int) string {
	if maxLength <= 0 {
		return ""
	}

	if len(value) <= maxLength {
		return strings.Trim(value, "-")
	}

	return strings.Trim(value[:maxLength], "-")
}

